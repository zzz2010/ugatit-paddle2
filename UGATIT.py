        self.genA2B.set_dict(params['genA2B'])
        self.genB2A.set_dict(params['genB2A'])
        self.disGA.set_dict(params['disGA'])
        self.disGB.set_dict(params['disGB'])
        self.disLA.set_dict(params['disLA'])
        self.disLB.set_dict(params['disLB'])

